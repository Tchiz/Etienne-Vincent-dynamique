<div class="biographie">
	<p>
		<strong>Etienne Vincent</strong> commence la guitare rock &agrave; treize ans et d&eacute;couvre ensuite le jazz gr&acirc;ce au guitariste Gilles Trial. 
	</p>
	<p>
		Il suit son enseignement &agrave; <strong>Jazz Action Valence</strong> jusqu' en 1999, puis &eacute;tudie &agrave; l'<strong>ENM de Villeurbanne</strong> et au <strong>CNR de Lyon</strong>. Il y obtient son <strong>DEM jazz</strong> en 2004.
	</p>
	<p>
		Sur Lyon, il participe &agrave; la cr&eacute;ation du <strong>collectif Polycarpe</strong>, se produit avec "Drawbars Organ trio " (avec <strong>David Bressat et Stephane Foucher</strong>), joue et enregistre un album avec <strong>N&eacute;o</strong> (groupe &eacute;lectro jazz lead&eacute; par D. Bressat), et avec son propre quartet (avec Remy Kapri&eacute;lan, Cl&eacute;ment Edouard et Basile Mouton). 
		Il se produit notamment au th&eacute;&acirc;tre de minuit pour <strong>jazz &agrave; Vienne 2005</strong>, au festival du p&eacute;ristyle de l'op&eacute;ra de Lyon ainsi qu'au Train Th&eacute;&acirc;tre (26).
	</p>
	<p>
		En 2007 il reprend le chemin de l’&eacute;cole, au <strong>Centre des musiques Didier Lockwood</strong>, ou il &eacute;tudie pendant un an aupr&egrave;s et des plus grands musiciens de jazz fran&ccedil;ais. Il sortira dipl&ocirc;m&eacute; avec mention. 
		Il poursuit parall&egrave;lement ses projets sur Valence, avec la chanteuse <strong>Anne Sila</strong>, et <strong>Siam trio</strong>.
	</p>
	<p>
		Actuellement il se produit principalement avec <strong>Mellophonic Quartet</strong> (hommage à Chet Baker) avec le chanteur Emmanuel Domergue en r&eacute;gion Rh&ocirc;ne alpes, et sur Paris, il multiplie les rencontres et se produit notamment avec <strong>Michel P&eacute;rez, Serge Merlaud, Fred Borey, Julien Pontvianne, Olivier Bog&eacute;</strong>… et bien sur avec son nouveau quartet.
	</p>
</div>
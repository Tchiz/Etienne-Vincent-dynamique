	<h2>Jazz Workshop Montrouge</h2>
	<h3>Atelier de jazz</h3>
	<p>
		<em>Pour musiciens amateurs ou aspirant pro</em>
		<em>Formations type "jazz combo", du quartet au sextet</em>
		<em>Travail d'un répertoire jazz et du jeu en groupe</em>
		<em>Mise en situation en concerts</em>
	<p>
		<em>Présentation</em>
		Ateliers jazz sur Montrouge, en partenariat avec les studios Music Live. 
		<strong>groupes de niveaux homogènes</strong><br>
		<strong>travail de l'écoute et du son d'ensemble du groupe.</strong><br>
		<strong>travail de l'improvisation et de l'interaction entre musiciens.</strong><br>
	</p>
	<p>
		De 4 a 6 personnes par groupe, dans l'esprit  " petite formation ", du type: guitare, piano, saxophone, contrebasse, batterie.
	</p>
	<p>
		Travail sur un répertoire jazz varié (standards, bossa, thèmes de compositeurs jazz tels que John Coltrane, Art Pepper, Dexter Gordon, Wes Montgomery, Herbie Hancock,etc.), suivant le niveau et les envies des élèves.
	</p>
	<p>
		Des concerts des ateliers ont lieux pendant l’année, donnant un objectif motivant aux cours.
	</p>
	<p>
		Il est demandé aux élèves un minimum de maitrise de leur instrument (pas de débutants). Les groupes sont constitués après un cours d'essai.
	</p>
	<p>
		<em>Horaires</em>
		<em>3 groupes à la rentrée 2013</em>
		<strong>Lundi</strong>: 20h à 22h <br/>
		<strong>Mardi</strong>: 20h à 22h <br/>
		<strong>Samedi</strong>: 11h à 13h
	</p>
	<p>
		<em>Studios</em>
		Les répétitions ont lieux aux studios music live de Montrouge (92), studios professionnels équipés de matériel haut de gamme
		(Fender twin, Mesa Boogie pour les guitaristes, amplis basse et batteries de qualité…).
		http://www.musiclive.fr/index.php
		Les studios sont à environ 7 minutes à pieds de la station de métro Chatillon-Montrouge (ligne 13), et de la station de métro Mairie de Montrouge (ligne 4).
		Parking sous terrain gratuit, réservé aux utilisateurs des studios.
	</p>
